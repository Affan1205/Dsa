#include <iostream>
using namespace std;
int main()
{

    int a = 9;
    int b = 11;
    cout << "AND of (a&b) is " << (a & b) << endl;
    cout << "OR of (a|b) is " << (a | b) << endl;
    cout << "NOT of (~a) is " << (~a) << endl;
    cout << "XOR of (a^b) is " << (a ^ b) << endl;

    return 0;
}